<h3 id="要求" tabindex="-1"><a class="header-anchor" href="#要求"><span>要求</span></a></h3>
<h4 id="基本要求" tabindex="-1"><a class="header-anchor" href="#基本要求"><span>基本要求</span></a></h4>
<ol>
<li>
<p>设置运动目标位置复位功能。执行此功能，红色光斑能从屏幕任意位置回到原点。光斑中心距原点误差≤2cm。</p>
</li>
<li>
<p>启动运动目标控制系统。红色光斑能在30秒内沿屏幕四周边线顺时针移动一周，移动时光斑中心距边线距离≤2cm。</p>
</li>
<li>
<p>用约1.8cm宽的黑色电工胶带沿A4纸四边贴一个长方形，构成A4靶纸。将此A4靶纸贴在屏幕自定的位置。启动运动目标控制系统，红色光斑能在30 秒内沿胶带顺时针移动一周。超时不得分，光斑完全脱离胶带一次扣 2 分，连续脱离胶带移动5cm以上记为0分。</p>
</li>
<li>
<p>将上述A4靶纸以任意旋转角度贴在屏幕任意位置。启动运动目标控制系统，要求同 3 。</p>
</li>
</ol>
<h4 id="发挥部分" tabindex="-1"><a class="header-anchor" href="#发挥部分"><span>发挥部分</span></a></h4>
<ol>
<li>
